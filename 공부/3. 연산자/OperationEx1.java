
public class OperationEx1 {

	public static void main(String[] args) {
		
		int num  = 13;
		//int / int -> int : 소수점 이하는 잘린다.
		System.out.println(num + " / 3 = " + (num / 3));
		
		// 나머지 
		System.out.println(num + " % 3 = " + (num % 3));
		
		// 나누기 연산은 0으로 할 수 없다.
		// System.out.println(num/0);
	}

}
